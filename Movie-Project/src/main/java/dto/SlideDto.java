package dto;

public class SlideDto {
	
	private String slide_title;
	private int slide_code;
	private String slide_img;
	private int slide_count;
	
	public String getSlide_title() {
		return slide_title;
	}
	public void setSlide_title(String slide_title) {
		this.slide_title = slide_title;
	}
	public int getSlide_code() {
		return slide_code;
	}
	public void setSlide_code(int slide_code) {
		this.slide_code = slide_code;
	}
	public String getSlide_img() {
		return slide_img;
	}
	public void setSlide_img(String slide_img) {
		this.slide_img = slide_img;
	}
	public int getSlide_count() {
		return slide_count;
	}
	public void setSlide_count(int slide_count) {
		this.slide_count = slide_count;
	}
	
	
}
