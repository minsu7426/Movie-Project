package dto;

public class CouponDto {
	private String coupon_code;
	private String coupon_form;
	private String coupon_give;
	private String coupon_end;
	private String coupon_id;
	private boolean coupon_flag;
	private int coupon_price;
	
	public int getCoupon_price() {
		return coupon_price;
	}
	public void setCoupon_price(int coupon_price) {
		this.coupon_price = coupon_price;
	}
	public String getCoupon_code() {
		return coupon_code;
	}
	public void setCoupon_code(String coupon_code) {
		this.coupon_code = coupon_code;
	}
	public String getCoupon_form() {
		return coupon_form;
	}
	public void setCoupon_form(String coupon_form) {
		this.coupon_form = coupon_form;
	}
	public String getCoupon_give() {
		return coupon_give;
	}
	public void setCoupon_give(String coupon_give) {
		this.coupon_give = coupon_give;
	}
	public String getCoupon_end() {
		return coupon_end;
	}
	public void setCoupon_end(String coupon_end) {
		this.coupon_end = coupon_end;
	}
	public String getCoupon_id() {
		return coupon_id;
	}
	public void setCoupon_id(String coupon_id) {
		this.coupon_id = coupon_id;
	}
	public boolean isCoupon_flag() {
		return coupon_flag;
	}
	public void setCoupon_flag(boolean coupon_flag) {
		this.coupon_flag = coupon_flag;
	}
	
	
}
